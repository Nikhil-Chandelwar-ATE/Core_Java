package decisionMakingStatement;

public class Switch_Case {

	public static void main(String[] args) {
		int a = 3;
		switch(a) {
		case 1 :{
			System.out.println("Hello");
		}
		break;
		case 2 : {
			System.out.println("Hi");
		}
		break;
		default : {
			System.out.println("Bye");
		}
		}
	}
}
